(function(){
  //$('.dropdown-toggle').dropdown();
  $('ul.dropdown-menu li a').on('click', function (event) {
    $('div#moduloOpciones').toggleClass('open');
  });
  $("a#agregarContactoBtn").click(function(e){
    e.preventDefault();
    e.returnValue=false;
    e.stopPropagation();
    aseguradoraProvider.config(true);
    if(aseguradoraProvider.showContacto){
      $("#vistaFormularioAseguradora").addClass('hide');
      $("#vistaFormularioContacto").removeClass('hide');
      $("input[id$='uuid_cliente]']").val(id_cliente);
    }
  });
  $("a#datosAseguradoraBtn").click(function(e){
    e.preventDefault();
    e.returnValue=false;
    e.stopPropagation();
    aseguradoraProvider.config(true);
    if(aseguradoraProvider.showAseguradora){
      $("#vistaFormularioAseguradora").removeClass('hide');
      $("#vistaFormularioContacto").addClass('hide');
      $("#vistaFormularioContacto").find("#cancelar").trigger('click');
      $("input[id$='uuid_cliente]']").val('');
    }
  });
})();
